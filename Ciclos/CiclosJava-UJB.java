public class Ciclos {
    public static void main(String[] args) {
        // Imprimir del 1 al 5
        // Ciclos while
        var contador = 1;
        final var VALOR_MAXIMO = 5;
        while(contador <= VALOR_MAXIMO){
            System.out.println("Valor: " + contador);
            contador++;
        }
    }
}
